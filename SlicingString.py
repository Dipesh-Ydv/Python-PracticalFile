name = 'Dipesh Yadav'

# It will print the whole string
print(name)

# It will print from index: 1 to 7
print(name[1:8]) 

# It will print the whole string by skipping 1 letter
print(name[::2])

# It will print name in reverse
print(name[-1:-13:-1])   

# It will also print in reverse
print(name[12::-1])     